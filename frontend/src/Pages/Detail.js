import { useEffect } from "react";
import { useParams } from "react-router-dom";
import axios from "axios";
import { useState } from "react";
import JsPDF from "jspdf";
import {Button} from "@mui/material";


function getDateFromTimestamp(timestamp) {
  const date = new Date(timestamp);
  const year = date.getFullYear();
  const month = date.getMonth() + 1; // Months are zero-based, so add 1
  const day = date.getDate();

  // Format the date as desired
  const formattedDate = `${year}-${month.toString().padStart(2, "0")}-${day
    .toString()
    .padStart(2, "0")}`;

  return formattedDate;
}

const Detail = () => {
  const param = useParams();
  const [manager, setManager] = useState();
  const [root, setRoot] = useState();
  const [data, setData] = useState([]);
  const name = localStorage.getItem("child-name");
  const caseNumber = localStorage.getItem("case-number");

  const childID = param.id;
  console.log(childID);
  useEffect(() => {
    const handleCellClick = async () => {
      try {
        const result = await axios.post(
          "http://localhost:4000/api/get-case-detail",
          { childID }
        );
        const workerDetail = result.data.worker;
        const processDetail = result.data.process;
        const managerDetail = result.data.caseManager;
        setManager(managerDetail);
        setRoot(workerDetail);
        setData(processDetail);

        console.log(workerDetail, processDetail, managerDetail);
        // SHOW THIS IN PDF AS DOWNLOADABLE
      } catch (err) {
        alert(err.message);
      }
    };
    handleCellClick();
  }, []);

  const generatePdf = () => {
    try {
      const report = new JsPDF("portrait", "pt", [795.28, 1241.89]);

      report.html(document.querySelector("#report"), {}).then(() => {
        report.save();
      });
    } catch (err) {
      alert(err.response.data.message);
    }
  };

  const download = (e, value) => {
    var base64String = value;

    console.log(base64String);
    const byteCharacters = atob(base64String);
    const byteArrays = [];

    for (let i = 0; i < byteCharacters.length; i++) {
      byteArrays.push(byteCharacters.charCodeAt(i));
    }

    const byteArray = new Uint8Array(byteArrays);
    const blob = new Blob([byteArray], { type: "application/pdf" });
    const url = URL.createObjectURL(blob);
    window.open(url, "_blank");
  };

  return (
    <>
      <div
        style={{
          display: "flex",
          justifyContent: "center",
          alignItems: "center",
          flexDirection: "column",
        }}
      >
        
        <div
          id="report"
          style={{
            marginTop: "40px",
            width: "210mm",
            height: "260mm",
            display: "flex",
            flexDirection: "column",
            alignItems: "center",
          }}
        >
          <div style={{marginBottom:'40px'}} >
            <p style={{ fontSize: "18px", fontWeight: "bold" }}>
              Case Number : {caseNumber}
            </p>
            <p style={{ fontSize: "18px", fontWeight: "bold" }}>
              Child Name : {name}
            </p>
            <p style={{ fontSize: "18px", fontWeight: "bold" }}>
              Social Worker : {root != null ? root.username : ""}
            </p>
            <p style={{ fontSize: "18px", fontWeight: "bold" }}>
              Case Manager : {manager != null ? manager.username : ""}
            </p>
          </div>
          <table style={{ width: "100%", borderCollapse: "collapse" }}>
            <thead>
              <tr>
                <th
                  style={{
                    padding: "10px",
                    textAlign: "left",
                    borderBottom: "1px solid #ccc",
                  }}
                >
                  Steps
                </th>
                <th
                  style={{
                    padding: "10px",
                    textAlign: "left",
                    borderBottom: "1px solid #ccc",
                  }}
                >
                  Type
                </th>
                <th
                  style={{
                    padding: "10px",
                    textAlign: "left",
                    borderBottom: "1px solid #ccc",
                  }}
                >
                  Completed On
                </th>
                <th
                  style={{
                    padding: "10px",
                    textAlign: "left",
                    borderBottom: "1px solid #ccc",
                  }}
                >
                  Value
                </th>
              </tr>
            </thead>
            <tbody>
              {data.map((item, index) => (
                <tr key={index}>
                  <td
                    style={{ padding: "10px", borderBottom: "1px solid #ccc" }}
                  >
                    {item.name}
                  </td>
                  <td
                    style={{ padding: "10px", borderBottom: "1px solid #ccc" }}
                  >
                    {item.type}
                  </td>
                  <td
                    style={{ padding: "10px", borderBottom: "1px solid #ccc" }}
                  >
                    {getDateFromTimestamp(item.date)}
                  </td>
                  {item.type == "pdf" ? (
                    <td
                      style={{
                        padding: "10px",
                        borderBottom: "1px solid #ccc",
                      }}
                      onClick={(e) => download(e, item.value)}
                    >
                      Download PDF
                    </td>
                  ) : (
                    <td
                      style={{
                        padding: "10px",
                        borderBottom: "1px solid #ccc",
                      }}
                    >
                      {item.value}
                    </td>
                  )}
                </tr>
              ))}
            </tbody>
          </table>
          
        </div>
        <div style={{marginBottom:'100px'}}>
          <Button
            variant="contained"
            onClick={generatePdf}
            sx={{
              bgcolor: "#382A41",
              fontSize: "15px",
              ":hover": {
                bgcolor: "#CD366B",
                color: "white",
              },
            }}
          >
            Download Report
          </Button>
        </div>
        
      </div>
      
    </>
  );
};

export default Detail;
